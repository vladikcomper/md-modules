; ---------------------------------------------------------------
; Subroutine to generate 1bpp decode table
; ---------------------------------------------------------------
; INPUT:
;		a1		Address for the generated table
;		d4	.w	Pixel index for bit #0
;		d5	.w	Pixel index for bit #1
;
; USES:
;		d0-d7
; ---------------------------------------------------------------

Generate1bppDecodeTable:
	lsl.w	#5, d5
	lea		@BaseDecodeTable(pc, d5.w), a2
	lsl.w	#5, d4
	beq.s	@DirectTransfer
	lea		@BaseDecodeTable(pc, d4.w), a3

	movem.l	(a2)+, d0-d3
	movem.l	$10(a3), d4-d7
	swap	d7
	or.l	d7, d0
	swap	d6
	or.l	d6, d1
	swap	d5
	or.l	d5, d2
	swap	d4
	or.l	d4, d3
	movem.l	d0-d3, (a0)

	movem.l	(a2)+, d0-d3
	movem.l	(a3), d4-d7
	swap	d7
	or.l	d7, d0
	swap	d6
	or.l	d6, d1
	swap	d5
	or.l	d5, d2
	swap	d4
	or.l	d4, d3
	movem.l	d0-d3, $10(a0)
	rts

; ---------------------------------------------------------------
@DirectTransfer:
	movem.l	(a2), d0-d7
	movem.l	d0-d7, (a0)
	rts

